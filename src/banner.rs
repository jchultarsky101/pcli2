use color_print::cprintln;
use pcli2::terminal::colors_enabled;

/// Display the ASCII art banner for PCLI2
pub fn print_banner() {
    if colors_enabled() {
        print_banner_colored();
    } else {
        print_banner_plain();
    }
}

